public void detplatform (int xpos, int ypos, int len, int[] col){
tint(col[0], col[1], col[2]);
image(rightplatform,xpos-x+590+len-20,500+ht);
  for(int i = 20; i < len-20; i++){
    image(middleplatform,xpos-x+590+i,500+ht-ypos);
  }
image(rightplatform,xpos-x+590+len-20,500+ht);
}
boolean falldown = false;
public void platform(int xpos, int ypos, int len, int[] col){
  tint(col[0], col[1], col[2]);
image(leftplatform,xpos-x+590,500+ht-ypos);
  for(int i = 20; i < len-20; i++){
    image(middleplatform,xpos-x+590+i,500+ht-ypos);
  }
image(rightplatform,xpos-x+590+len-20,500+ht-ypos);
  if(x < xpos+len-50 && x > xpos-100 && ht > -50+ypos){
    falldown = false;
  }
}
