public void draw(){
  falldown = true;
  if(keyPressed){
  if(key == 'a'){
    xvelocity -= 2;
  }
  if(key == 'd'){
    xvelocity += 2;
  }
  if(key == 'w' && jumping == false){
    yvelocity = 10;
    jumping = true;
  }
}
  if(start == false){
  background(0,255,255);
  fill(255);
  textSize(20);
  text("Your Score: "+score,50,30);
  text("Your High-Score:"+highscore,50,50);
  text("Universal High-Score:"+unihscr.read(),50,70);
  textSize(210);
  text("Platformer!",70,300);
  noStroke();
  fill(0,255,0);
  rect(50,350,1180,300);
  fill(100);
  text("Start!",300,600);
  }
  else{
    if(jumping == true){
      yvelocity -= 0.3f;
      ht += yvelocity;
    }
    if(ht < -150){
      if(highscore < score){
        highscore = score;
      }
      if(unihscr.read() == -1){
        unihscr.write(score+"");
      }
      else if(PApplet.parseInt(unihscr.read()) < score){
        unihscr.write(score+"");
      }
      start = false;
      x = 0;
      ht = 0;
      yvelocity = 0;
      xvelocity = 0;
      jumping = false;
      saveStuff();
    }
    background(0,255,255);
    textSize(30);
    text("Your Score Is "+score+"!",300,200);
    if(score > highscore && score < 300+highscore){
      text("You Beat Your Highscore!",300,220);
    }
    fill(0,255,0);
    col1[0] = 0;
    col1[1] = 0;
    col1[2] = 255;
    for(int n = 0; n < k.length/2; n++){
      if(colch == "b->g"){
      col1[2] -= 5;
      col1[1] += 5;
      if(col1[2] <= 0){
        colch = "g->r";
      }
      }
      else if(colch == "g->r"){
        col1[1] -= 5;
        col1[0] += 5;
        if(col1[1] <= 0){
          colch = "r->b";
        }
      }
      else{
        col1[0] -= 5;
        col1[2] += 5;
        if(col1[0] <= 0){
          colch = "b->g";
        }
      }
      if(k[2*n] < x+720 && k[2*n]+k[2*n+1] > x-720){
      platform(k[2*n], pfrmht[n], k[(2*n)+1], col1);
      }
      if(jumping == true){
        if(x < k[2*n] + k[2*n+1]-50 && x > k[2*n]-100 && ht <= pfrmht[n] && ht >= pfrmht[n]-50){
          ht = pfrmht[n];
          jumping = false;
          yvelocity = 0;
          score = ht;
        }
      } 
    }
    if(falldown == true && jumping == false){
      yvelocity -= 0.3f;
      ht += yvelocity;
    }
    xvelocity -= (xvelocity/10);
    if(xvelocity < 0){
      left = true;
    }
    if(xvelocity > 0){
      left = false;
    }
    tint(255);
    if(left == true){
    image(lookleft,640,450);
    }
    else{
      image(lookright,640,450);
    }
    x+=xvelocity;
  }
}
public void saveStuff(){
  JSONObject saves = new JSONObject();
  saves.setLong("Score",score);
  saves.setLong("High-Score",highscore);
  saveJSONObject(saves, "saves.json");
}
public void loadStuff(){
  score = saves.getLong("Score");
  highscore = saves.getLong("High-Score");
}
public void mousePressed(){
  if(mouseX>360 && start == false){
    start = true;
    score = 0;
    i = 0;
    j = 1280;
    l = 0;
    for(int n = 0; n < k.length/2; n++){
      k[2*n] = (i);
      k[2*n+1] = (j);
      pfrmht[n] = l;
      l += random(50,150);
      if(l < -100){
        l = -100;
      }
      i+= j;
      i+= random(200,500);
      j = round(random(200,500));
    }
  }
}
